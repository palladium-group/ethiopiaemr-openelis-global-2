package org.openelisglobal.reportdefinition.form;

import java.sql.Timestamp;
import org.openelisglobal.common.form.BaseForm;

/**
 * DTO for transferring ReportDefinition data via REST API.
 *
 * <p>
 * Simple data carrier with no business logic.
 */
public class ReportDefinitionForm extends BaseForm {

    private String id;

    private String name;

    private String description;

    private String category;

    private String definitionJson;

    private String createdBy;

    private Timestamp createdDate;

    private Timestamp lastupdated;

    private Boolean isActive;

    public ReportDefinitionForm() {
        super();
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getDefinitionJson() {
        return definitionJson;
    }

    public void setDefinitionJson(String definitionJson) {
        this.definitionJson = definitionJson;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public Timestamp getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Timestamp createdDate) {
        this.createdDate = createdDate;
    }

    public Timestamp getLastupdated() {
        return lastupdated;
    }

    public void setLastupdated(Timestamp lastupdated) {
        this.lastupdated = lastupdated;
    }

    public Boolean getIsActive() {
        return isActive;
    }

    public void setIsActive(Boolean isActive) {
        this.isActive = isActive;
    }
}
